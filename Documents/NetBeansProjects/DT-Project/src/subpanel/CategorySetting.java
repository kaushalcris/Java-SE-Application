    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String mc = String.valueOf(jComboBox2.getSelectedItem());
        String sc = String.valueOf(jComboBox1.getSelectedItem());

        if (mc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Main Category name is empty", "Warning",
                    JOptionPane.WARNING_MESSAGE);
        } else if (sc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sub Category name is empty",
                    "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet results = MySQL.execute("SELECT * FROM `category` "
                        + "INNER JOIN `main_category` ON `category`.`mc_id`=`main_category`.`mc_id`"
                        + "INNER JOIN `sub_category` ON `category`.`sc_id` = `sub_category`.`sc_id` "
                        + "WHERE `mc_name`='" + mc + "' AND `sc_name`= '" + sc + "'");

                if (results.next()) {
                    JOptionPane.showMessageDialog(this, "Category already exists",
                            "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.execute("INSERT INTO `category` (`mc_id`,`sc_id`) VALUES ('" + mainCategoryMap.get(mc) + "','" + subCategoryMap.get(sc) + "')");
                    resetCategorySetting();
                    loadCategoryTable("SELECT * FROM `category` "
                            + "INNER JOIN `main_category` ON `category`.`mc_id`=`main_category`.`mc_id`"
                            + "INNER JOIN `sub_category` ON `category`.`sc_id` = `sub_category`.`sc_id`");
                    JOptionPane.showMessageDialog(this, "Category added successfully",
                            "Information", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                SignIn.log1.warning(e.toString());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

