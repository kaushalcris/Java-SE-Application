    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:

        int option = JOptionPane.showConfirmDialog(this, "Are You Sure to Change Status Of This Order", "Message", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (option == JOptionPane.YES_OPTION) {
            try {
                ResultSet resultset = MySQL.execute("SELECT * FROM `other_service` WHERE `barcode`='" + customOrderBarcode + "'");
                if (resultset.next()) {
                    String updatingOrderStatus = "0";
                    if (resultset.getString("order_status_id").equals("1")) {
                        updatingOrderStatus = "2";
                    } else if (resultset.getString("order_status_id").equals("2")) {
                        updatingOrderStatus = "1";
                    }
                    MySQL.execute("UPDATE `other_service` SET `order_status_id`='" + updatingOrderStatus + "' "
                            + "WHERE `barcode`='" + customOrderBarcode + "' ");
                    loadCustomProduct(customOrderBarcode);
                    customManagement.loadCustomServicesTable();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton5ActionPerformed

