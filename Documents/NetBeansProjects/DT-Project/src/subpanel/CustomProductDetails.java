    private void loadCustomProduct(String barcode) {

        jLabel1.setText("Custom Order : " + barcode);

        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM `other_service`"
                    + "INNER JOIN `customer` ON `other_service`.`customer_mobile` = `customer`.`mobile` "
                    + "INNER JOIN `order_status` ON `other_service`.`order_status_id` = `order_status`.`id` "
                    + "WHERE `barcode`='" + barcode + "' ");
            if (resultset.next()) {

                Double amount = Double.parseDouble(resultset.getString("amount"));
                int qty = Integer.parseInt(resultset.getString("qty"));
                totalAmount = amount * qty;
                Double advancePayment = Double.parseDouble(resultset.getString("payment"));
                Double balance = totalAmount - advancePayment;

                jLabel9.setText(": " + resultset.getString("barcode"));
                jLabel21.setText(": " + resultset.getString("mobile"));
                jLabel23.setText(": " + resultset.getString("fname") + " " + resultset.getString("lname"));
                jLabel15.setText(": " + resultset.getString("order_date"));
                jLabel17.setText(": " + resultset.getString("deliver_date"));
                jTextArea1.setText(": " + resultset.getString("description"));
                jLabel10.setText(": " + Dashboard.FORMAT.format(Double.parseDouble(resultset.getString("amount"))));
                jLabel18.setText(": " + resultset.getString("qty"));

                jLabel11.setText(Dashboard.FORMAT.format(totalAmount));
                jLabel12.setText(Dashboard.FORMAT.format(advancePayment));
                jLabel13.setText(Dashboard.FORMAT.format(balance));

                if (resultset.getString("order_status.id").equals("1")) {
                    jLabel24.setForeground(new java.awt.Color(102, 0, 0));
                } else if (resultset.getString("order_status.id").equals("2")) {
                    jLabel24.setForeground(new java.awt.Color(0, 102, 0));
                }
                jLabel24.setText(resultset.getString("status"));

                if (balance == 0.0) {
                    jButton6.setEnabled(false);
                } else {
                    jButton6.setEnabled(true);
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

